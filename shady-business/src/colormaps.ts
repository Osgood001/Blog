const blendToWhite = (colormap: number[][]): number[][] => {
  const N = colormap.length;
  const newMap = [...colormap];
  const iStart = Math.round(N * 0.9);
  for (let i = iStart; i < N; i++) {
    const fracWhite = (i - iStart) / (N - 1 - iStart);
    newMap[i] = [
      fracWhite + (1 - fracWhite) * colormap[i][0],
      fracWhite + (1 - fracWhite) * colormap[i][1],
      fracWhite + (1 - fracWhite) * colormap[i][2],
    ];
  }
  return newMap;
};

const makeColormap = (colormap: number[][]) => (i: number): number[] => {
  if (Number.isNaN(i)) {
    return colormap[0];
  }
  if (i < 0) {
    return colormap[0];
  }

  if (i >= 1) {
    return colormap[colormap.length - 1];
  }

  const element = colormap[Math.floor(i * colormap.length)];

  return element;
};

